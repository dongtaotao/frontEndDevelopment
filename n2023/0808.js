js加载执行：async, defer, preload, prefetch的区别？

async：立即下载（异步，不会阻碍文档解析），异步执行（执行的时候会阻碍文档解析）
defer：立即下载（异步，不会阻碍文档解析），延迟执行，在整个页面都解析完毕后执行
preload：提前下载，需要的时候立即执行，无需再下载
prefetch：提前下载，在未来的某个页面可能会执行，节省下载时间

